int		ft_putchar(char c);

void	ft_is_negative(int n)
{
	char n_;
	char p_;

	n_ = 'N';
	p_ = 'P';
	if (n < 0)
		ft_putchar(n_);
	if (n >= 0)
		ft_putchar(p_);
}
